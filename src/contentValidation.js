/**
 * ЭТАП 1: Нормоконтроль содержания — выявление вымышленных данных.
 * Запрещены: выдуманные ИНН/ОГРН, несуществующие нормативные акты,
 * фейковая статистика без источника, выдуманные результаты эксперимента.
 */
import { isValidInn } from './dataIntegrity.js';

// Несуществующие/устаревшие нормативные шаблоны (пример заведомо фейковых номеров)
const SUSPICIOUS_NORM_RE = [
  /ФЗ[\s-]*№?\s*9\d{3}/i,           // нереалистично большие номера ФЗ
  /ГОСТ\s+Р?\s*\d{6,}/i,            // слишком длинные номера ГОСТ
  /СанПиН\s+\d{1,2}\.\d{1,2}\.\d{4,}/i,
];

// Паттерны выдуманной статистики «по ощущениям» (процент без указания источника рядом)
const STAT_RE = /(\d{1,3}(?:[.,]\d+)?)\s*%/g;
const SOURCE_NEAR_RE = /(росстат|данны\w+|источник|по\s+данным|согласно|исследовани\w+|n\s*=|выборк|таблиц)/i;

/** Найти проценты без ссылки на источник в том же/соседнем предложении. */
function findUnsourcedStats(text) {
  const issues = [];
  const sentences = String(text || '').split(/(?<=[.!?])\s+/);
  for (const s of sentences) {
    const stats = s.match(STAT_RE);
    if (stats && stats.length && !SOURCE_NEAR_RE.test(s)) {
      issues.push(s.trim().slice(0, 120));
    }
  }
  return issues;
}

/** Проверить блоки на признаки вымышленных данных. */
export function validateContent(blocks, { strict = false } = {}) {
  const errors = [];
  const warnings = [];

  for (let i = 0; i < blocks.length; i += 1) {
    const b = blocks[i];
    const text = b.text || (b.rows ? b.rows.flat().join(' ') : '');
    if (!text) continue;

    // 1. Фейковые ИНН/ОГРН (любые цифры после «ИНН», кроме корректных 10/12)
    const innMatch = text.match(/ИНН\s*[:№]?\s*(\d[\d\s]{0,13}\d)/i);
    if (innMatch) {
      const inn = innMatch[1].replace(/\s/g, '');
      if (!isValidInn(inn)) {
        errors.push(`Блок ${i}: некорректный ИНН ${inn}`);
      }
    }

    // 2. Подозрительные нормативные акты
    for (const re of SUSPICIOUS_NORM_RE) {
      const m = text.match(re);
      if (m) warnings.push(`Блок ${i}: подозрительный норм. акт «${m[0]}» — проверить существование`);
    }

    // 3. Статистика без источника (только в абзацах)
    if (b.kind === 'p') {
      const unsourced = findUnsourcedStats(text);
      if (unsourced.length && strict) {
        unsourced.forEach((s) => warnings.push(`Блок ${i}: процент без источника — «${s}»`));
      }
    }
  }

  // 4. «данные отсутствуют» — допустимая честная формулировка, не ошибка
  return {
    ok: errors.length === 0,
    errors,
    warnings,
  };
}

