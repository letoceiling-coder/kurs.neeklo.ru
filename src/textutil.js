/**
 * Очистка текста от markdown-разметки и служебных символов.
 * Возвращает чистый русский академический текст без *, #, .md и пр.
 */
export function cleanText(input) {
  if (!input) return '';
  let t = String(input);

  // нормализуем переносы строк
  t = t.replace(/\r\n/g, '\n').replace(/\r/g, '\n');

  // убираем кодовые ограждения ```...```
  t = t.replace(/```[a-zA-Z]*\n?/g, '');

  // заголовки markdown: "### Текст" -> "Текст"
  t = t.replace(/^\s{0,3}#{1,6}\s*/gm, '');

  // жирный/курсив: **текст**, __текст__, *текст*, _текст_
  t = t.replace(/\*\*\*(.+?)\*\*\*/g, '$1');
  t = t.replace(/\*\*(.+?)\*\*/g, '$1');
  t = t.replace(/\*(.+?)\*/g, '$1');
  t = t.replace(/___(.+?)___/g, '$1');
  t = t.replace(/__(.+?)__/g, '$1');
  // одиночные подчёркивания внутри слов оставляем редко — убираем парные
  t = t.replace(/(?<!\w)_(.+?)_(?!\w)/g, '$1');

  // инлайн-код `текст`
  t = t.replace(/`([^`]+)`/g, '$1');

  // маркеры списков в начале строки: -, *, +, • -> убираем (нумерацию оставляем)
  t = t.replace(/^\s*[-*+•]\s+/gm, '');

  // markdown-ссылки [текст](url) -> текст
  t = t.replace(/\[([^\]]+)\]\(([^)]+)\)/g, '$1');

  // горизонтальные линии --- ___ ***
  t = t.replace(/^\s*([-_*])\1{2,}\s*$/gm, '');

  // markdown-таблицы (строки с |) — оставляем как есть, обработаем отдельно

  // упоминания форматов файлов как артефакты: ".md", "markdown"
  t = t.replace(/\.md\b/gi, '');
  t = t.replace(/\bmarkdown\b/gi, '');

  // двойные пробелы
  t = t.replace(/[ \t]{2,}/g, ' ');

  // лишние пустые строки (3+ -> 2)
  t = t.replace(/\n{3,}/g, '\n\n');

  return t.trim();
}

/**
 * Разбивает текст на абзацы (массив строк).
 */
export function toParagraphs(input) {
  const clean = cleanText(input);
  return clean
    .split(/\n+/)
    .map((s) => s.trim())
    .filter((s) => s.length > 0);
}

/**
 * Транслитерация-безопасное имя файла.
 */
export function safeFileName(name) {
  return String(name)
    .replace(/[\\/:*?"<>|]+/g, '_')
    .replace(/\s+/g, '_')
    .slice(0, 120);
}
