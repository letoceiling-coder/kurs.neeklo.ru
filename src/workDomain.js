export function domainStructureHint(domain, cfg) {
  if (domain === WORK_DOMAINS.pedagogy) return pedagogyOutlineHint(cfg);
  if (domain === WORK_DOMAINS.law) {
    return `Глава 1 — теория права и доктрина; глава 2 — правоприменительная/судебная практика, анализ; глава ${cfg.chapters} — предложения по совершенствованию.`;
  }
  const base = cfg.subsectionsPerChapter > 0
    ? `Работа: ${cfg.chapters} глав, по ${cfg.subsectionsPerChapter} параграфа.
Глава 1 — теоретико-методологические основы.
Глава 2 — анализ объекта и практики.
Глава ${cfg.chapters} — разработка рекомендаций и оценка эффективности.`
    : `Работа: ${cfg.chapters} раздела.`;
  if (domain === WORK_DOMAINS.it) {
    return `${base}
Для IT-темы: глава 3 — РАЗРАБОТКА системы (архитектура, UML, БД); допустимы BPMN as-is/to-be.`;
  }
  if (domain === WORK_DOMAINS.economics) {
    return `${base}
Глава 2 — анализ предприятия/отрасли, показатели, проблемы; глава 3 — проект мероприятий, расчёт эффекта.`;
  }
  return base;
}
