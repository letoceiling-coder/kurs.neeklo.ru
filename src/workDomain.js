import { isAiTopic } from './researchContext.js';

/** Домены ВКР — от структуры зависит методология и лексика. */
export const WORK_DOMAINS = {
  pedagogy: 'pedagogy',
  it: 'it',
  economics: 'economics',
  law: 'law',
  general: 'general',
};

const PEDAGOGY_RE = /педагог|дошкольн|обучающ|воспитан|учащ|школьн|посткроссинг|развивающ|образовательн\w*\s+(сред|процесс|деятельност|организац)|методик\w*\s+(обуч|преподав|воспит)|фгос|урок|учебн\w*\s+завед|детск\w*\s+сад|\bдоу\b|компетенц|познават\w*\s+интерес|старш\w*\s+дошкольн|младш\w*\s+школьн|психолог\w*\s+развит/i;
const LAW_RE = /правов|юридич|законодат|норматив\w*\s+прав|уголовн|гражданск\w*\s+прав/i;
const IT_RE = /информационн|программ|автоматизац|\berp\b|баз\w*\s+данн|кибер|\bit\b|digital|нейросет|llm|rag|api[\s-]?gateway|архитектур\w*\s+систем/i;
const ECON_RE = /эконом|финанс|предприят|организац\w*\s+отрасл|маркетинг|менеджмент|бизнес[\s-]?процесс|мсп|малый\w*\s+бизнес/i;

/** Определить домен по теме и плану. Педагогика имеет приоритет над «экономикой». */
export function detectWorkDomain(topic, outline = {}) {
  const text = [
    topic,
    outline?.title,
    outline?.object,
    outline?.subject,
    ...(outline?.keywords || []),
    ...(outline?.tasks || []),
  ].filter(Boolean).join(' ');

  if (PEDAGOGY_RE.test(text)) return WORK_DOMAINS.pedagogy;
  if (LAW_RE.test(text)) return WORK_DOMAINS.law;
  if (isAiTopic(text) && IT_RE.test(text)) return WORK_DOMAINS.it;
  if (ECON_RE.test(text)) return WORK_DOMAINS.economics;
  if (IT_RE.test(text)) return WORK_DOMAINS.it;
  return WORK_DOMAINS.general;
}

export function isPedagogyDomain(domain) {
  return domain === WORK_DOMAINS.pedagogy;
}

export function isBusinessLikeDomain(domain) {
  return domain === WORK_DOMAINS.it || domain === WORK_DOMAINS.economics;
}

